import io
import re
from datetime import datetime

from docx import Document
from docx.shared import Pt, Inches
from docx.enum.text import WD_ALIGN_PARAGRAPH


ASSESSMENT_LABELS = {
    "phq9": ("PHQ-9 Depression Screening", 27),
    "gad7": ("GAD-7 Anxiety Screening", 21),
    "sdq": ("SDQ Child Behaviour Screening", 50),
}


def severity_label(assessment_type, score):
    if assessment_type == "phq9":
        if score <= 4:
            return "Minimal"
        if score <= 9:
            return "Mild"
        if score <= 14:
            return "Moderate"
        if score <= 19:
            return "Moderately Severe"
        return "Severe"
    if assessment_type == "gad7":
        if score <= 4:
            return "Minimal"
        if score <= 9:
            return "Mild"
        if score <= 14:
            return "Moderate"
        return "Severe"
    if assessment_type == "sdq":
        if score <= 13:
            return "Normal"
        if score <= 16:
            return "Borderline"
        return "Abnormal"
    return "Not classified"


def _plain_text(markdown_text):
    if not markdown_text:
        return ""
    text = re.sub(r"\*\*(.+?)\*\*", r"\1", markdown_text)
    text = re.sub(r"\*(.+?)\*", r"\1", text)
    text = re.sub(r"\[(.+?)\]\(.+?\)", r"\1", text)
    text = re.sub(r"^>\s?", "", text, flags=re.MULTILINE)
    return text.strip()


def build_assessment_report(user, record, assessment_kind="self"):
    title, max_score = ASSESSMENT_LABELS.get(record.assessment_type, ("Assessment", record.score))
    severity = severity_label(record.assessment_type, record.score)

    doc = Document()
    for section in doc.sections:
        section.top_margin = Inches(1)
        section.bottom_margin = Inches(1)
        section.left_margin = Inches(1)
        section.right_margin = Inches(1)

    heading = doc.add_heading("MindCare Mental Health Report", level=0)
    heading.alignment = WD_ALIGN_PARAGRAPH.CENTER

    doc.add_paragraph(f"Generated: {datetime.now().strftime('%d %B %Y, %H:%M')}")
    doc.add_paragraph(f"User: {user.get_full_name() or user.username}")
    doc.add_paragraph(f"Assessment: {title}")
    doc.add_paragraph(f"Date completed: {record.created_at.strftime('%d %B %Y')}")
    doc.add_paragraph("")

    doc.add_heading("Summary", level=1)
    doc.add_paragraph(f"Total score: {record.score} out of {max_score}")
    doc.add_paragraph(f"Severity level: {severity}")
    doc.add_paragraph(f"Age group: {record.age_group}")
    doc.add_paragraph(f"Sex: {record.sex}")

    if assessment_kind == "child":
        doc.add_paragraph(f"Difficulties subscale: {record.difficulties}")
        doc.add_paragraph(f"Prosocial subscale: {record.prosocial}")

    doc.add_paragraph("")
    doc.add_heading("Personalised Feedback", level=1)
    feedback = _plain_text(record.ai_response or "No feedback was recorded for this assessment.")
    for paragraph in feedback.split("\n"):
        if paragraph.strip():
            doc.add_paragraph(paragraph.strip())

    doc.add_paragraph("")
    doc.add_heading("Important Notice", level=1)
    doc.add_paragraph(
        "This report is generated by MindCare for personal awareness only. "
        "It is not a medical diagnosis. If you are concerned about your mental health, "
        "please speak with a qualified health professional or counsellor."
    )

    buffer = io.BytesIO()
    doc.save(buffer)
    buffer.seek(0)
    return buffer
